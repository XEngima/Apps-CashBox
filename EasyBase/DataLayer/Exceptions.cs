using System;
using System.Collections.Generic;
using System.Linq;
using System.Text;

namespace EasyBase.DataLayer
{
    public class DataLayerException : ApplicationException
    {
        public DataLayerException()
            : base()
        {
        }

        public DataLayerException(string message)
            : base(message)
        {
        }

        public DataLayerException(string message, Exception innerException)
            : base(message, innerException)
        {
        }
    }

    public class NotConnectedToReleaseDatabaseException : DataLayerException
    {
        public NotConnectedToReleaseDatabaseException()
            : base()
        {
        }

        public NotConnectedToReleaseDatabaseException(string message)
            : base(message)
        {
        }

        public NotConnectedToReleaseDatabaseException(string message, Exception innerException)
            : base(message, innerException)
        {
        }
    }
}

