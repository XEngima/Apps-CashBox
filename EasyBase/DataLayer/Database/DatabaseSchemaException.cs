using System;
using System.Collections.Generic;
using System.Linq;
using System.Text;

namespace EasyBase.DataLayer
{
	public class DatabaseSchemaException : ApplicationException
	{
		public DatabaseSchemaException()
			:base()
		{
		}

		public DatabaseSchemaException(string message)
			: base(message)
		{
		}

		public DatabaseSchemaException(string message, Exception innerException)
			: base(message, innerException)
		{
		}
	}
}
